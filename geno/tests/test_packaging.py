def test_publish_workflow_scopes_oidc_to_shell_free_publish_job():
    workflow = _publish_workflow()
    jobs = workflow["jobs"]

    assert workflow["permissions"] == {"contents": "read"}
    assert jobs["publish"]["permissions"]["id-token"] == "write"
    assert all(
        value != "write"
        for name, value in jobs["publish"]["permissions"].items()
        if name != "id-token"
    )
    assert all(
        job_name == "publish" or job.get("permissions", {}).get("id-token") != "write"
        for job_name, job in jobs.items()
    )

    publish_steps = jobs["publish"]["steps"]
    assert len(publish_steps) == 2
    assert all("run" not in step for step in publish_steps)
    for step in publish_steps:
        action, separator, ref = step["uses"].partition("@")
        assert separator == "@", action
        assert len(ref) == 40 and all(char in "0123456789abcdef" for char in ref)


def test_publish_workflow_transfers_the_smoked_artifact_without_rebuild():
    jobs = _publish_workflow_jobs()
    build_steps = _step_names(jobs["build"])
    publish_steps = _step_names(jobs["publish"])
    upload = _step_by_name(jobs["build"], "Upload tested distributions")
    download = _step_by_name(jobs["publish"], "Download tested distributions")

    assert build_steps.count("Build package") == 1
    assert "Build package" not in publish_steps
    assert upload["with"]["name"] == download["with"]["name"]
    assert upload["with"]["path"] == "dist/"
    assert download["with"] == {"name": "python-distributions", "path": "dist/"}


def test_docker_build_context_and_copy_are_allowlisted():
    repo_root = Path(__file__).resolve().parents[2]
    dockerfile = (repo_root / "Dockerfile").read_text(encoding="utf-8")
    dockerignore = (repo_root / ".dockerignore").read_text(encoding="utf-8")
    dependabot = yaml.safe_load(
        (repo_root / ".github" / "dependabot.yml").read_text(encoding="utf-8")
    )

    from_lines = [line for line in dockerfile.splitlines() if line.startswith("FROM ")]
    assert len(from_lines) == 2
    assert all(
        re.fullmatch(
            r"FROM python:3\.11-slim@sha256:[0-9a-f]{64} AS (builder|runtime)",
            line,
        )
        for line in from_lines
    )
    assert "COPY --chown=geno:geno . /app" not in dockerfile
    assert "COPY --from=builder" in dockerfile
    assert "python -m twine check --strict dist/*" in dockerfile

    ignore_lines = set(dockerignore.splitlines())
    assert dockerignore.splitlines()[2] == "*"
    assert "!geno/**" not in ignore_lines
    assert not any("geno/tests" in line for line in ignore_lines)
    assert {
        "!pyproject.toml",
        "!requirements.lock",
        "!requirements-release.lock",
        "!geno/server.py",
        "!geno/_darwin_resource.py",
        "!geno/cli/run.py",
        "!geno/std/List.geno",
    } <= ignore_lines
    assert any(
        update.get("package-ecosystem") == "docker" and update.get("directory") == "/"
        for update in dependabot["updates"]
    )


