def test_publish_workflow_hash_locks_build_tools_and_validates_metadata():
    jobs = _publish_workflow_jobs()

    job = jobs["build"]
    install = _step_by_name(job, "Install build tools")["run"]
    validate = _step_by_name(job, "Validate artifact metadata")

    assert install == (
        "python -m pip install --require-hashes -r requirements-release.lock"
    )
    assert validate["run"] == "python -m twine check --strict dist/*"

    repo_root = Path(__file__).resolve().parents[2]
    release_lock = (repo_root / "requirements-release.lock").read_text(encoding="utf-8")
    assert "autogenerated by pip-compile with Python 3.11" in release_lock
    for tool in ("build", "setuptools", "twine", "wheel"):
        assert f"{tool}==" in release_lock
    for linux_dependency in ("importlib-metadata", "jeepney", "secretstorage"):
        assert f"{linux_dependency}==" in release_lock
    assert "--hash=sha256:" in release_lock


